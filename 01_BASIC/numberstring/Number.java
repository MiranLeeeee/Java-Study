package numberstring;

public class Number {
	
	public static void main(String[] args) {
		/*숫자형태의 정수 실수는 연산자를 통해 연산됨*/
		//데이터타입: 정수
		System.out.println(10+2);//덧셈
		System.out.println(10-2);//뺄셈
		System.out.println(10*2);//곱셈
		System.out.println(10/2);//몫
		System.out.println(10%2);//나머지
		//데이터타입: 실수
		System.out.println(2.5+3.8);
	}	
	
}
