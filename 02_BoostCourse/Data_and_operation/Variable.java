
public class Variable {
	public static void main(String[] args) {
		//데이터타입을 명시해서 변수를 선언
		
		//Number -> integer (음수, 0, 양수)
		int a = 1;
		System.out.println(a);
		
		//Real number (실수) -> double
		double b = 1.1;
		System.out.println(b);
		
		//String (문자열)
		String c = "Hello World";
		System.out.println(c);
		
	}
}
