
public class Number {
	public static void main(String[] args) {
		// Operator (사칙연산)
		System.out.println(6+2); //8
		System.out.println(6-2); //4
		System.out.println(6*2); //12
		System.out.println(6/2); //3
		
		//Math: 수학과 관련된 라이브러리
		System.out.println(Math.PI);//3.141592653589793 (PI값을 가져옴)
		System.out.println(Math.floor(Math.PI));//3.0 (내림: 소수점을 잘라버림)
		System.out.println(Math.ceil(Math.PI));//4.0 (올림: 소수점을 올려버림)
	}
}
